package session03;

public class ForLoopDemo {
    public static void main(String[] args) {
        /**
         * syntax:
         *
         * for(initialization; condition; incr/decr)
         * {
         * 	line of codes
         * }
         */
        for(int i=10; i>0; i--)
        {
            System.out.println(i);
            //System.out.println(2 * i -1);//odd number

        }
    }
}
