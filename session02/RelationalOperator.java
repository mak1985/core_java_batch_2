package session02;

public class RelationalOperator {
    public static void main(String[] args) {
        /**
         * Relational operator - ( ==, !=, >, <, >=, and <=)
         */

        int a = 10, b =20;

        System.out.println(a==b); //false
        System.out.println(a!=b); // true
        System.out.println(a>b); //false
        System.out.println(a<b);//true
        System.out.println(a>=b);//false
        System.out.println(a<=b);//true
    }
}
