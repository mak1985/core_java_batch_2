package session02;

public class StringConcatenation {
    public static void main(String[] args) {
        String firstname = "Makhan";
        String lastname = "Dhar";

        System.out.println(firstname+lastname); //MakhanDhar
        System.out.println(firstname+"           "+lastname);//Makhan          Dhar
        System.out.println("This is me: "+firstname+" "+lastname);//Makhan Dhar
    }
}
